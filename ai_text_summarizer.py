import gradio as gr
from transformers import pipeline
import nltk

nltk.download('punkt')

summarizer = pipeline("summarization")

def summarize_text(text):
    if len(text) < 50:
        return "âš ï¸ Text is too short to summarize. Please enter a longer text."

    summary = summarizer(text, max_length=150, min_length=50, do_sample=False)
    return summary[0]['summary_text']

interface = gr.Interface(
    fn=summarize_text,
    inputs=gr.Textbox(lines=5, placeholder="ðŸ“ Enter text to summarize..."),
    outputs="text",
    title="ðŸ“ AI Text Summarizer",
    description="Enter a long paragraph, and the AI will generate a concise summary.",
)

interface.launch()